"""cProfile the task-spawn workloads across stdlib / uvloop / ferro_io.

Runs `make_task_spawn` and `make_taskgroup_spawn` from bench_primitives.py under
cProfile. Each (column, workload) pair runs in its own subprocess so
uvloop.install() / ferro_io.install() can't contaminate neighbours. Output is a
markdown table per pair at benchmarks/PROFILE.md, plus a trailing Findings
section that the operator fills in after inspection.
"""
from __future__ import annotations

import argparse
import cProfile
import io
import json
import pstats
import subprocess
import sys
from pathlib import Path

HERE = Path(__file__).resolve().parent
SELF = Path(__file__).resolve()
REPORT = HERE / "PROFILE.md"

COLUMNS = ["stdlib", "uvloop", "ferro_io"]
WORKLOADS = ["task_spawn", "taskgroup_spawn"]


def _load_asyncio(column: str):
    if column == "stdlib":
        import asyncio
        return asyncio
    if column == "uvloop":
        import uvloop  # type: ignore
        uvloop.install()
        import asyncio
        return asyncio
    if column == "ferro_io":
        import ferro_io
        ferro_io.install()
        import asyncio
        return asyncio
    raise ValueError(column)


def _workload_fn(asyncio, name: str):
    # Import lazily so the child doesn't load bench_primitives until after
    # the column's asyncio is wired up.
    sys.path.insert(0, str(HERE))
    from bench_primitives import make_task_spawn, make_taskgroup_spawn
    if name == "task_spawn":
        return make_task_spawn(asyncio)
    if name == "taskgroup_spawn":
        return make_taskgroup_spawn(asyncio)
    raise ValueError(name)


def _run_one(column: str, workload: str) -> list[dict]:
    asyncio = _load_asyncio(column)
    fn = _workload_fn(asyncio, workload)
    fn()  # warmup
    prof = cProfile.Profile()
    prof.enable()
    fn()
    prof.disable()
    buf = io.StringIO()
    pstats.Stats(prof, stream=buf).sort_stats("cumulative").print_stats(25)
    raw = buf.getvalue()
    rows: list[dict] = []
    for line in raw.splitlines():
        stripped = line.strip()
        if not stripped or not stripped[0].isdigit():
            continue
        # pstats columns: ncalls tottime percall cumtime percall filename:lineno(function)
        parts = stripped.split(None, 5)
        if len(parts) < 6:
            continue
        try:
            ncalls = parts[0]
            tottime = float(parts[1])
            cumtime = float(parts[3])
        except ValueError:
            continue
        func = parts[5]
        rows.append({"ncalls": ncalls, "tottime": tottime, "cumtime": cumtime, "func": func})
    return rows


def main() -> None:
    p = argparse.ArgumentParser(description=__doc__)
    p.add_argument("--only", choices=COLUMNS, default=None)
    p.add_argument("--workload", choices=WORKLOADS, default=None)
    p.add_argument("--json", action="store_true")
    args = p.parse_args()

    if args.only and args.workload:
        if args.only == "uvloop":
            try:
                import uvloop  # type: ignore  # noqa: F401
            except ImportError:
                if args.json:
                    print(json.dumps({"column": "uvloop", "workload": args.workload,
                                      "available": False, "rows": []}))
                return
        rows = _run_one(args.only, args.workload)
        if args.json:
            print(json.dumps({"column": args.only, "workload": args.workload,
                              "available": True, "rows": rows}))
        return

    results: list[dict] = []
    for col in COLUMNS:
        for wl in WORKLOADS:
            print(f"profiling {col} / {wl} ...", file=sys.stderr)
            proc = subprocess.run(
                [sys.executable, str(SELF), "--only", col, "--workload", wl, "--json"],
                capture_output=True, text=True, cwd=str(HERE),
            )
            if proc.returncode != 0:
                print(f"[warn] {col}/{wl} failed (rc={proc.returncode}): {proc.stderr}",
                      file=sys.stderr)
                continue
            for line in proc.stdout.splitlines():
                if line.startswith("{"):
                    results.append(json.loads(line))
                    break

    lines: list[str] = ["# ferro_io task-spawn cProfile report\n"]
    lines.append("Top 25 functions by cumulative time per (column, workload).")
    lines.append("Generated by `python benchmarks/profile_task_spawn.py`.\n")
    for r in results:
        header = f"## {r['column']} — {r['workload']}"
        if not r.get("available", True):
            lines.append(f"\n{header}\n\n_column unavailable_\n")
            continue
        lines.append(f"\n{header}\n")
        lines.append("| ncalls | tottime | cumtime | function |")
        lines.append("|---|---:|---:|---|")
        for row in r["rows"]:
            func_escaped = row["func"].replace("|", "\\|")
            lines.append(
                f"| {row['ncalls']} | {row['tottime']:.4f} | {row['cumtime']:.4f} | `{func_escaped}` |"
            )

    lines.append("\n## Findings\n")
    lines.append("_Filled in after running. See the plan file for the questions this should answer._\n")

    REPORT.write_text("\n".join(lines) + "\n")
    print(f"wrote {REPORT}")


if __name__ == "__main__":
    main()
